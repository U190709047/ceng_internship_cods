public class FourTransactions {

    public int plus(int num1 , int num2){
        return num1+num2;
    }
    public int minus(int num1 , int num2){
        return num1-num2;
    }
    public int divide(int num1 , int num2){
        return num1/num2;
    }
    public int times(int num1 , int num2){
        return num1*num2;
    }
}
